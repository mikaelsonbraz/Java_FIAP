package cap_02;

public class operadores {

    public static void main(String[] args){

        int x = 5;
        int y = 10;
        int z = 20;

        int resultSoma = x + y;
        int resultSubtracao = z - y;
        int resultMult = x * z;
        int resultDiv = z / y;
        int resultResto = 8 % 3;

        System.out.println("Soma: " + resultSoma +
                "\nSubtração: " + resultSubtracao +
                "\nMultiplicação: " + resultMult +
                "\nDivisão: " + resultDiv +
                "\nResto da divisão de 8 por 3: " + resultResto);


    }
}
